    /**
     * Gets card blocking instructions (generic, no brand names).
     *
     * @return List of steps to block a compromised card
     */
    fun getCardBlockingSteps(): List<String> {
        return listOf(
            "1. Locate the customer service number on the back of your card",
            "2. Call immediately — most banks have 24/7 helplines for card blocking",
            "3. Tell the representative you need to block your card due to suspected fraud",
            "4. Note down the complaint/reference number provided",
            "5. If available, use your banking app's instant card lock feature",
            "6. Request confirmation via SMS/email that the card has been blocked",
            "7. Ask about the timeline for issuing a replacement card",
            "8. Inquire about the dispute resolution process for fraudulent charges"
        )
    }

    /**
     * Registers a merchant as known/trusted (won't flag in future).
     *
     * @param merchantName The merchant name to trust
     */
    fun addKnownMerchant(merchantName: String) {
        val merchants = loadKnownMerchants().toMutableSet()
        merchants.add(merchantName.lowercase().trim())
        prefs.edit().putStringSet(KEY_KNOWN_MERCHANTS, merchants).apply()
    }

    /**
     * Removes a merchant from the trusted list.
     *
     * @param merchantName The merchant name to un-trust
     */
    fun removeKnownMerchant(merchantName: String) {
        val merchants = loadKnownMerchants().toMutableSet()
        merchants.remove(merchantName.lowercase().trim())
        prefs.edit().putStringSet(KEY_KNOWN_MERCHANTS, merchants).apply()
    }

    /**
     * Gets detection statistics.
     *
     * @return Map with stats about alerts generated, confirmed fraud, etc.
     */
    fun getStats(): Map<String, Any> {
        val allAlerts = loadAlerts()
        return mapOf(
            "totalAlerts" to allAlerts.size,
            "activeAlerts" to allAlerts.count { !it.isDismissed },
            "dismissedAlerts" to allAlerts.count { it.isDismissed },
            "confirmedFraud" to allAlerts.count { it.isConfirmedFraud },
            "highSeverity" to allAlerts.count { it.severity == Severity.HIGH },
            "mediumSeverity" to allAlerts.count { it.severity == Severity.MEDIUM },
            "lowSeverity" to allAlerts.count { it.severity == Severity.LOW },
            "knownMerchants" to loadKnownMerchants().size,
            "transactionsAnalyzed" to prefs.getInt(KEY_TRANSACTION_COUNT, 0)
        )
    }

    /**
     * Updates the running average transaction amount with a new data point.
     * Used internally to calibrate the "unusual amount" detector.
     *
     * @param amount The new transaction amount to incorporate
     */
    fun updateAverageAmount(amount: Double) {
        val currentAvg = prefs.getFloat(KEY_AVERAGE_AMOUNT, 0f).toDouble()
        val count = prefs.getInt(KEY_TRANSACTION_COUNT, 0)

        val newCount = count + 1
        val newAvg = if (count == 0) amount
        else ((currentAvg * count) + amount) / newCount

        prefs.edit()
            .putFloat(KEY_AVERAGE_AMOUNT, newAvg.toFloat())
            .putInt(KEY_TRANSACTION_COUNT, newCount)
            .apply()
    }

    /**
     * Clears all fraud detection data. Use with caution.
     */
    fun clearAllData() {
        prefs.edit().clear().apply()
    }

