package com.paisabrain.app.safety

import android.content.Context
import android.content.SharedPreferences
import com.google.gson.Gson
import com.google.gson.reflect.TypeToken
import java.time.LocalDateTime
import java.time.LocalTime
import java.time.format.DateTimeFormatter
import java.time.temporal.ChronoMinutes
import kotlin.math.abs

/**
 * Detects potentially fraudulent or suspicious transactions using
 * pattern analysis and behavioral heuristics.
 *
 * This operates entirely on-device with no external API calls.
 * It analyzes transaction patterns against the user's normal behavior
 * to flag anomalies that may indicate unauthorized activity.
 *
 * Detection methods:
 * - Unusual transaction times (late night activity)
 * - Unusual amounts (significantly above average)
 * - Unknown merchants with high amounts
 * - Rapid successive debits
 * - International transaction patterns
 * - Location anomalies
 *
 * All analysis is privacy-preserving and runs locally.
 */
class FraudDetector(private val context: Context) {

    companion object {
        private const val PREFS_NAME = "fraud_detector_prefs"
        private const val KEY_ALERTS = "fraud_alerts"
        private const val KEY_KNOWN_MERCHANTS = "known_merchants"
        private const val KEY_TRANSACTION_HISTORY = "recent_transactions"
        private const val KEY_AVERAGE_AMOUNT = "average_transaction_amount"
        private const val KEY_TRANSACTION_COUNT = "transaction_count"
        private const val KEY_DISMISSED_ALERTS = "dismissed_alerts"

        /** Late night hours considered unusual (2 AM to 5 AM) */
        private val UNUSUAL_TIME_START = LocalTime.of(2, 0)
        private val UNUSUAL_TIME_END = LocalTime.of(5, 0)

        /** Multiplier above average that triggers amount flag */
        private const val AMOUNT_MULTIPLIER_THRESHOLD = 3.0

        /** Number of rapid transactions that trigger flag */
        private const val RAPID_DEBIT_COUNT = 3

        /** Time window for rapid debits (in minutes) */
        private const val RAPID_DEBIT_WINDOW_MINUTES = 5L

        /** Amount threshold for unknown merchant flagging (in rupees) */
        private const val UNKNOWN_MERCHANT_AMOUNT_THRESHOLD = 5000.0

        /** Maximum alerts to store */
        private const val MAX_STORED_ALERTS = 100
    }

    private val prefs: SharedPreferences =
        context.getSharedPreferences(PREFS_NAME, Context.MODE_PRIVATE)
    private val gson = Gson()
    private val dateTimeFormatter = DateTimeFormatter.ISO_LOCAL_DATE_TIME

    /**
     * Severity levels for fraud alerts.
     *
     * @property displayName Human-readable severity label
     * @property color Hex color for UI (red/orange/yellow)
     * @property priority Numeric priority for sorting (higher = more severe)
     */
    enum class Severity(val displayName: String, val color: String, val priority: Int) {
        /** Slightly unusual but likely legitimate */
        LOW(
            displayName = "Unusual",
            color = "#FFC107",
            priority = 1
        ),

        /** Suspicious pattern that warrants attention */
        MEDIUM(
            displayName = "Suspicious",
            color = "#FF9800",
            priority = 2
        ),

        /** Strong indicators of potential fraud — immediate action recommended */
        HIGH(
            displayName = "Likely Fraud",
            color = "#F44336",
            priority = 3
        )
    }

    /**
     * Types of fraud detection rules.
     */
    enum class DetectionType(val displayName: String, val emoji: String) {
        UNUSUAL_TIME("Unusual Time", "🌙"),
        UNUSUAL_AMOUNT("Unusual Amount", "💸"),
        UNKNOWN_MERCHANT("Unknown Merchant", "❓"),
        RAPID_DEBITS("Rapid Debits", "⚡"),
        INTERNATIONAL("International Transaction", "🌍"),
        LOCATION_ANOMALY("Location Anomaly", "📍")
    }

    /**
     * Represents a single financial transaction for analysis.
     *
     * @property id Unique transaction identifier
     * @property amount Transaction amount (positive = debit, negative = credit)
     * @property merchant Merchant or recipient name
     * @property timestamp When the transaction occurred (ISO datetime)
     * @property category Spending category if known
     * @property isInternational Whether this is an international transaction
     * @property location City/country if available
     */
    data class Transaction(
        val id: String,
        val amount: Double,
        val merchant: String,
        val timestamp: String,
        val category: String = "",
        val isInternational: Boolean = false,
        val location: String = ""
    )

    /**
     * A fraud alert generated by the detection system.
     *
     * @property id Unique alert identifier
     * @property transaction The transaction that triggered the alert
     * @property severity How serious the alert is
     * @property detectionType What kind of pattern was detected
     * @property reason Human-readable explanation of why this was flagged
     * @property suggestedAction What the user should do
     * @property timestamp When the alert was generated
     * @property isDismissed Whether the user has dismissed this alert
     * @property isConfirmedFraud Whether user confirmed this was fraudulent
     */
    data class FraudAlert(
        val id: String,
        val transaction: Transaction,
        val severity: Severity,
        val detectionType: DetectionType,
        val reason: String,
        val suggestedAction: String,
        val timestamp: String,
        val isDismissed: Boolean = false,
        val isConfirmedFraud: Boolean = false
    )

    /**
     * Result of analyzing a transaction.
     *
     * @property isSafe Whether no issues were detected
     * @property alerts List of alerts generated (may be multiple per transaction)
     * @property overallSeverity The highest severity among all alerts
     */
    data class AnalysisResult(
        val isSafe: Boolean,
        val alerts: List<FraudAlert>,
        val overallSeverity: Severity?
    )

    /**
     * Steps to take when fraud is suspected.
     */
    data class EmergencySteps(
        val immediateActions: List<String>,
        val followUpActions: List<String>,
        val preventionTips: List<String>
    )

    // ─────────────────────────────────────────────────────────────
    // Public API
    // ─────────────────────────────────────────────────────────────

    /**
     * Analyzes a single transaction for potential fraud indicators.
     * Runs all detection rules and returns any alerts generated.
     *
     * @param transaction The transaction to analyze
     * @return Analysis result with any alerts
     */
    fun analyzeTransaction(transaction: Transaction): AnalysisResult {
        val alerts = mutableListOf<FraudAlert>()

        // Run all detection checks
        checkUnusualTime(transaction)?.let { alerts.add(it) }
        checkUnusualAmount(transaction)?.let { alerts.add(it) }
        checkUnknownMerchant(transaction)?.let { alerts.add(it) }
        checkRapidDebits(transaction)?.let { alerts.add(it) }
        checkInternational(transaction)?.let { alerts.add(it) }

        // Record this transaction for future pattern analysis
        recordTransaction(transaction)

        // Store any alerts generated
        if (alerts.isNotEmpty()) {
            storeAlerts(alerts)
        }

        val overallSeverity = alerts.maxByOrNull { it.severity.priority }?.severity

        return AnalysisResult(
            isSafe = alerts.isEmpty(),
            alerts = alerts,
            overallSeverity = overallSeverity
        )
    }

    /**
     * Analyzes a batch of transactions (e.g., after syncing).
     *
     * @param transactions List of transactions to analyze
     * @return Map of transaction ID to analysis result (only flagged ones)
     */
    fun analyzeBatch(transactions: List<Transaction>): Map<String, AnalysisResult> {
        val flagged = mutableMapOf<String, AnalysisResult>()

        transactions.forEach { transaction ->
            val result = analyzeTransaction(transaction)
            if (!result.isSafe) {
                flagged[transaction.id] = result
            }
        }

        return flagged
    }

    /**
     * Gets all active (non-dismissed) fraud alerts.
     *
     * @return List of alerts sorted by severity (highest first)
     */
    fun getActiveAlerts(): List<FraudAlert> {
        return loadAlerts()
            .filter { !it.isDismissed }
            .sortedByDescending { it.severity.priority }
    }

    /**
     * Gets all alerts including dismissed ones.
     *
     * @return Complete alert history
     */
    fun getAllAlerts(): List<FraudAlert> {
        return loadAlerts().sortedByDescending { it.timestamp }
    }

    /**
     * Dismisses an alert (user reviewed and confirmed it's legitimate).
     *
     * @param alertId The alert ID to dismiss
     */
    fun dismissAlert(alertId: String) {
        val alerts = loadAlertsMutable()
        val index = alerts.indexOfFirst { it.id == alertId }
        if (index >= 0) {
            alerts[index] = alerts[index].copy(isDismissed = true)
            saveAlerts(alerts)
        }

        // Add merchant to known list if it was an unknown merchant alert
        val alert = alerts.find { it.id == alertId }
        if (alert?.detectionType == DetectionType.UNKNOWN_MERCHANT) {
            addKnownMerchant(alert.transaction.merchant)
        }
    }

    /**
     * Confirms an alert as actual fraud.
     *
     * @param alertId The alert ID to confirm
     */
    fun confirmFraud(alertId: String) {
        val alerts = loadAlertsMutable()
        val index = alerts.indexOfFirst { it.id == alertId }
        if (index >= 0) {
            alerts[index] = alerts[index].copy(isConfirmedFraud = true)
            saveAlerts(alerts)
        }
    }

