    // Detection Rules
    // ─────────────────────────────────────────────────────────────

    /**
     * Checks if a transaction occurred during unusual hours (2 AM - 5 AM).
     */
    private fun checkUnusualTime(transaction: Transaction): FraudAlert? {
        val time = try {
            LocalDateTime.parse(transaction.timestamp, dateTimeFormatter).toLocalTime()
        } catch (e: Exception) {
            return null
        }

        if (time.isAfter(UNUSUAL_TIME_START) && time.isBefore(UNUSUAL_TIME_END)) {
            val severity = if (transaction.amount > 10000) Severity.MEDIUM else Severity.LOW

            return FraudAlert(
                id = "alert_${transaction.id}_time",
                transaction = transaction,
                severity = severity,
                detectionType = DetectionType.UNUSUAL_TIME,
                reason = "Transaction at ${time.hour}:${time.minute.toString().padStart(2, '0')} — " +
                        "most of your transactions happen during daytime hours. " +
                        "Late-night transactions can indicate unauthorized card usage.",
                suggestedAction = if (severity == Severity.MEDIUM) {
                    "Check if you recognize this transaction. If not, consider blocking your card."
                } else {
                    "Verify this was you. Late-night transactions are sometimes legitimate (online shopping, travel)."
                },
                timestamp = LocalDateTime.now().format(dateTimeFormatter)
            )
        }
        return null
    }

    /**
     * Checks if the transaction amount is unusually high compared to average.
     */
    private fun checkUnusualAmount(transaction: Transaction): FraudAlert? {
        val averageAmount = prefs.getFloat(KEY_AVERAGE_AMOUNT, 0f).toDouble()
        val transactionCount = prefs.getInt(KEY_TRANSACTION_COUNT, 0)

        // Need at least 10 transactions to establish a baseline
        if (transactionCount < 10 || averageAmount <= 0) return null

        val multiplier = transaction.amount / averageAmount

        if (multiplier >= AMOUNT_MULTIPLIER_THRESHOLD) {
            val severity = when {
                multiplier >= 10.0 -> Severity.HIGH
                multiplier >= 5.0 -> Severity.MEDIUM
                else -> Severity.LOW
            }

            return FraudAlert(
                id = "alert_${transaction.id}_amount",
                transaction = transaction,
                severity = severity,
                detectionType = DetectionType.UNUSUAL_AMOUNT,
                reason = "This transaction of ₹${formatAmount(transaction.amount)} is " +
                        "${multiplier.toInt()}x your average spending of ₹${formatAmount(averageAmount)}. " +
                        "Unusually large transactions can indicate unauthorized access.",
                suggestedAction = when (severity) {
                    Severity.HIGH -> "Block your card immediately and contact your bank. " +
                            "This is significantly above your normal spending pattern."
                    Severity.MEDIUM -> "Verify this transaction immediately. If you don't recognize it, " +
                            "block your card using the number on the back."
                    Severity.LOW -> "Check if you recognize this purchase. Large but not extreme — " +
                            "could be a planned expense."
                },
                timestamp = LocalDateTime.now().format(dateTimeFormatter)
            )
        }
        return null
    }

    /**
     * Checks if the merchant is unknown AND the amount is high.
     */
    private fun checkUnknownMerchant(transaction: Transaction): FraudAlert? {
        val knownMerchants = loadKnownMerchants()
        val merchantLower = transaction.merchant.lowercase().trim()

        if (merchantLower in knownMerchants) return null
        if (transaction.amount < UNKNOWN_MERCHANT_AMOUNT_THRESHOLD) return null

        val severity = when {
            transaction.amount > 20000 -> Severity.HIGH
            transaction.amount > 10000 -> Severity.MEDIUM
            else -> Severity.LOW
        }

        return FraudAlert(
            id = "alert_${transaction.id}_merchant",
            transaction = transaction,
            severity = severity,
            detectionType = DetectionType.UNKNOWN_MERCHANT,
            reason = "First-time transaction with '${transaction.merchant}' for ₹${formatAmount(transaction.amount)}. " +
                    "You've never transacted with this merchant before, and the amount is significant.",
            suggestedAction = "Check if you recognize this merchant. " +
                    "If this is legitimate, you can mark it as known. " +
                    "If not, contact your bank immediately.",
            timestamp = LocalDateTime.now().format(dateTimeFormatter)
        )
    }

    /**
     * Checks for rapid successive debit transactions (possible card skimming).
     */
    private fun checkRapidDebits(transaction: Transaction): FraudAlert? {
        val recentTransactions = loadRecentTransactions()

        val transactionTime = try {
            LocalDateTime.parse(transaction.timestamp, dateTimeFormatter)
        } catch (e: Exception) {
            return null
        }

        // Count transactions in the last N minutes
        val recentCount = recentTransactions.count { recent ->
            try {
                val recentTime = LocalDateTime.parse(recent.timestamp, dateTimeFormatter)
                val minutesDiff = abs(
                    java.time.Duration.between(recentTime, transactionTime).toMinutes()
                )
                minutesDiff <= RAPID_DEBIT_WINDOW_MINUTES && recent.amount > 0
            } catch (e: Exception) {
                false
            }
        }

        if (recentCount >= RAPID_DEBIT_COUNT - 1) { // -1 because current transaction counts too
            val totalAmount = recentTransactions
                .filter { recent ->
                    try {
                        val recentTime = LocalDateTime.parse(recent.timestamp, dateTimeFormatter)
                        val minutesDiff = abs(
                            java.time.Duration.between(recentTime, transactionTime).toMinutes()
                        )
                        minutesDiff <= RAPID_DEBIT_WINDOW_MINUTES
                    } catch (e: Exception) {
                        false
                    }
                }
                .sumOf { it.amount } + transaction.amount

            return FraudAlert(
                id = "alert_${transaction.id}_rapid",
                transaction = transaction,
                severity = Severity.HIGH,
                detectionType = DetectionType.RAPID_DEBITS,
                reason = "${recentCount + 1} transactions within $RAPID_DEBIT_WINDOW_MINUTES minutes " +
                        "totaling ₹${formatAmount(totalAmount)}. " +
                        "Rapid successive debits are a strong indicator of card compromise or skimming.",
                suggestedAction = "Block your card immediately! Multiple rapid transactions " +
                        "are a common sign of card fraud. Call the number on the back of your card NOW.",
                timestamp = LocalDateTime.now().format(dateTimeFormatter)
            )
        }
        return null
    }

    /**
     * Checks for international transaction patterns.
     */
    private fun checkInternational(transaction: Transaction): FraudAlert? {
        if (!transaction.isInternational) return null

        // Check if user typically makes international transactions
        val recentTransactions = loadRecentTransactions()
        val recentInternational = recentTransactions.count { it.isInternational }
        val isUnusual = recentInternational == 0 // No recent international transactions

        if (isUnusual) {
            val severity = when {
                transaction.amount > 50000 -> Severity.HIGH
                transaction.amount > 10000 -> Severity.MEDIUM
                else -> Severity.LOW
            }

            return FraudAlert(
                id = "alert_${transaction.id}_intl",
                transaction = transaction,
                severity = severity,
                detectionType = DetectionType.INTERNATIONAL,
                reason = "International transaction of ₹${formatAmount(transaction.amount)} " +
                        "${if (transaction.location.isNotEmpty()) "from ${transaction.location}" else ""}. " +
                        "You don't typically make international purchases, which makes this unusual.",
                suggestedAction = if (severity == Severity.HIGH) {
                    "If you're not currently abroad or didn't make an international purchase, " +
                            "block your card immediately."
                } else {
                    "Verify this is your transaction. If you're traveling or made an online " +
                            "purchase from an international seller, this may be normal."
                },
                timestamp = LocalDateTime.now().format(dateTimeFormatter)
            )
        }
        return null
    }

    // ─────────────────────────────────────────────────────────────
    // Persistence Helpers
    // ─────────────────────────────────────────────────────────────

    private fun loadAlerts(): List<FraudAlert> {
        val json = prefs.getString(KEY_ALERTS, null) ?: return emptyList()
        return try {
            val type = object : TypeToken<List<FraudAlert>>() {}.type
            gson.fromJson(json, type)
        } catch (e: Exception) {
            emptyList()
        }
    }

    private fun loadAlertsMutable(): MutableList<FraudAlert> {
        return loadAlerts().toMutableList()
    }

    private fun saveAlerts(alerts: List<FraudAlert>) {
        val trimmed = if (alerts.size > MAX_STORED_ALERTS) {
            alerts.takeLast(MAX_STORED_ALERTS)
        } else {
            alerts
        }
        prefs.edit().putString(KEY_ALERTS, gson.toJson(trimmed)).apply()
    }

    private fun storeAlerts(newAlerts: List<FraudAlert>) {
        val existing = loadAlertsMutable()
        existing.addAll(newAlerts)
        saveAlerts(existing)
    }

    private fun loadKnownMerchants(): Set<String> {
        return prefs.getStringSet(KEY_KNOWN_MERCHANTS, emptySet()) ?: emptySet()
    }

    private fun recordTransaction(transaction: Transaction) {
        val recent = loadRecentTransactionsMutable()
        recent.add(transaction)

        // Keep only last 50 for pattern analysis
        val trimmed = if (recent.size > 50) {
            recent.takeLast(50).toMutableList()
        } else {
            recent
        }

        prefs.edit().putString(KEY_TRANSACTION_HISTORY, gson.toJson(trimmed)).apply()

        // Update average
        if (transaction.amount > 0) {
            updateAverageAmount(transaction.amount)
        }
    }

    private fun loadRecentTransactions(): List<Transaction> {
        val json = prefs.getString(KEY_TRANSACTION_HISTORY, null) ?: return emptyList()
        return try {
            val type = object : TypeToken<List<Transaction>>() {}.type
            gson.fromJson(json, type)
        } catch (e: Exception) {
            emptyList()
        }
    }

    private fun loadRecentTransactionsMutable(): MutableList<Transaction> {
        return loadRecentTransactions().toMutableList()
    }

    /**
     * Formats a numeric amount for display.
     */
    private fun formatAmount(amount: Double): String {
        return if (amount == amount.toLong().toDouble()) {
            amount.toLong().toString()
        } else {
            String.format("%.2f", amount)
        }
    }
}
